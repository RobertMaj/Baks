    public void akcjaDodajFirme() {
        czytajFormatke();
        if (company.getName() == null || company.getName().isEmpty()) {
            BaksSessionBean.getInstance().fireMessage(this, "Dodanie nowej firmy", "Wpisz nazwe firmy!");
            return;
        }
        try {
            daoFactory.getDaoInvoice().dodajFirme(connection, company);
            BaksSessionBean.getInstance().fireMessage(this, "Zapis", "Poprawnie zapisano nowÄ… firme.");
            this.dispose();
        } catch (Exception ex) {

        }
    }

    public void czytajFormatke() {
        company = new TO_PaymentCompany();
        company.setName(textNazwaFirmy.getText());
        company.setAdress(textAdresFirmy.getText());
        company.setPhone(textTelefon.getText());
        company.setNip(textNip.getText());
    }

    public void akcjaWyjdz() {
        this.dispose();
    }

